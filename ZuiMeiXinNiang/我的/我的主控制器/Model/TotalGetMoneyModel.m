#import "TotalGetMoneyModel.h"

@implementation TotalGetMoneyModel
-(NSString *)gain{
    if ([NSString isNULL:_gain]) {
        return @"+0.000";
    }else{
        return [NSString stringWithFormat:@"%@",_gain];
    }
}
@end
